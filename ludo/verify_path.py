def check_path(path):
    errors = []
    if len(path) != 52:
        errors.append(f'长度错误: {len(path)} != 52')
    for i in range(len(path)):
        cur = path[i]
        nxt = path[(i+1) % len(path)]
        dist = abs(cur[0]-nxt[0]) + abs(cur[1]-nxt[1])
        if dist != 1:
            errors.append(f'步骤{i}({cur})->步骤{(i+1)%52}({nxt}) 距离={dist}')
    seen = set()
    for i, p in enumerate(path):
        if p in seen:
            errors.append(f'重复: {p} at {i}')
        seen.add(p)
    return errors

